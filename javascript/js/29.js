// Math 객체

Math.ceil(0.5);                      // 올림    // 1
Math.round(0.5);                     // 반올림  // 1
Math.floor(0.5);                     // 내림    // 0

Math.random();                       // 0 ~ 1 사이의 랜덤값
Math.floor(Math.random() * 10) + 1;  // 1 ~ 10 사이의 랜덤값

Math.max(1,2,3,4,5);                 // 파라미터 내 최대값 반환 // 5
Math.min(1,2,3,4,5);                 // 파라미터 내 최소값 반환 // 1

Math.abs(-1);                        // 절대값 반환 // 1